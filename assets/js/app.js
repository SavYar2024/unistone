document.addEventListener("DOMContentLoaded", () => {
  const nodes = document.querySelectorAll('[data-i18n]');
  const langButtons = document.querySelectorAll('[data-lang-btn]');
  const langInputs = document.querySelectorAll('.lang-input');

  function applyFieldTranslations(lang){
    document.querySelectorAll('[data-placeholder-uk]').forEach(el => {
      const value = el.getAttribute(`data-placeholder-${lang}`) || el.getAttribute('data-placeholder-uk');
      if (value) el.placeholder = value;
    });

    document.querySelectorAll('option[data-uk], option[data-option-uk]').forEach(el => {
      const value = el.getAttribute(`data-${lang}`) || el.getAttribute(`data-option-${lang}`) || el.getAttribute('data-uk') || el.getAttribute('data-option-uk');
      if (value) el.textContent = value;
    });
  }

  function setLang(lang){
    const pack = dict[lang] || dict.uk;

    nodes.forEach(node => {
      const key = node.dataset.i18n;
      if (pack[key]) node.textContent = pack[key];
    });

    applyFieldTranslations(lang);

    langButtons.forEach(btn => {
      btn.classList.toggle('active', btn.dataset.langBtn === lang);
    });

    langInputs.forEach(input => input.value = lang);

    document.documentElement.lang = lang;
    localStorage.setItem('unistoneLang', lang);
  }

  langButtons.forEach(btn => {
    btn.addEventListener('click', () => setLang(btn.dataset.langBtn));
  });

  const nav = document.getElementById('mainNav');
  const burger = document.getElementById('burger');
  if (burger && nav) {
    burger.addEventListener('click', ()=> nav.classList.toggle('open'));
  }

  const toast = document.getElementById('toast');
  function showToast(message){
    if (!toast) return;
    toast.textContent = message;
    toast.classList.add('show');
    setTimeout(()=>toast.classList.remove('show'), 3600);
  }

  document.querySelectorAll('.lead-form').forEach(form => {
    form.addEventListener('submit', async e => {
      e.preventDefault();
      const fd = new FormData(form);
      try {
        const res = await fetch(form.action, { method:'POST', body:fd });
        const data = await res.json();
        if (data.ok) {
          showToast(`${data.message} — ${data.lead_id}`);
          form.reset();
          setLang(localStorage.getItem('unistoneLang') || 'uk');
        } else {
          showToast(data.message || 'Error');
        }
      } catch(e) {
        showToast('Network error. Please try again.');
      }
    });
  });

  document.querySelectorAll('.btn-hero').forEach(btn => {
    btn.addEventListener('click', function(e){
      const href = this.getAttribute('href');
      if (!href || !href.startsWith('#')) return;
      const target = document.querySelector(href);
      if (!target) return;
      e.preventDefault();
      const y = target.getBoundingClientRect().top + window.pageYOffset - 110;
      window.scrollTo({ top: y, behavior: 'smooth' });
    });
  });

  setLang(localStorage.getItem('unistoneLang') || 'uk');
});
